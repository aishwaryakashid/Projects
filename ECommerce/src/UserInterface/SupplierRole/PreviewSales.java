  public void  populatepreviewTable()
    {
                int rowCount = previewTable.getRowCount();

        DefaultTableModel model = (DefaultTableModel)previewTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
            model.removeRow(i);
        }
        for(Product p : productCatalog.getProductCatalog()) {
            int avail = p.getInitialAvail() - p.getAvailability();
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getInitialAvail() - p.getAvailability();
            row[2] = p.getPrice()* avail;
            model.addRow(row);
            }
      
        
        RowSorter<TableModel> sorter = new TableRowSorter<TableModel>(model);
        previewTable.setAutoCreateRowSorter(true);
    previewTable.setRowSorter(sorter);
    
    List sortKeys = new ArrayList();
sortKeys.add(new RowSorter.SortKey(1, SortOrder.DESCENDING));
sorter.setSortKeys(sortKeys);
    
    }
        
    
