        for(Product p : productCatalog.getProductCatalog()) {
                        int avail = p.getInitialAvail() - p.getAvailability();

            dataset.setValue(avail, "Quantity", p.getProdName());
            
           
        }
        JFreeChart chart = ChartFactory.createBarChart("Product Preformance Review", "Products", "Quantity", dataset,PlotOrientation.VERTICAL,false,true,false);
