    private void deactivateJBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deactivateJBtn1ActionPerformed
        // TODO add your handling code here:
        int selectedRow= userAccountJTable.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Select a row to disable the user");
            return;
        }
        else
        {
            UserAccount ua = (UserAccount)userAccountJTable.getValueAt(selectedRow, 0);
            ua.setStatus("Disabled");
            JOptionPane.showMessageDialog(null, "User Account is deactivated");
            populateUserAccountJTable();
        }
    }//GEN-LAST:event_deactivateJBtn1ActionPerformed

