    private void addUserAccJBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserAccJBtn1ActionPerformed
        // TODO add your handling code here:
        boolean isContain =false;
        String usern = userNameTxt.getText();
        String password = passwordTxt.getText();
         Role role = (Role)roleJComboBox.getSelectedItem();
             Person p = (Person) personJCombobox1.getSelectedItem();

           String status;
            if (activeJRadioBtn.isSelected()) {
                status = "Active" ;
            } else {
                status = "Disabled";
            }

        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
            for(UserAccount userA : organization.getUserAccountDirectory().getUserAccountList())
             {
            if (usern.equals(userA.getUsername()))
            {
                isContain =true;

                break;

            }
        }
            if(isContain)
        {
            JOptionPane.showMessageDialog(null, "Username already exists");
           return;
           
        }
            else
            {
             organization.getUserAccountDirectory().createUserAccount(usern, password, p, role, status);
            }
        
        
       
       
    }//GEN-LAST:event_addUserAccJBtn1ActionPerformed

