    private void populateRoleComboBox(Organization organization){
        roleJComboBox.removeAllItems();
        for (Role role : organization.getSupportedRoles()){
            roleJComboBox.addItem(role);
        }
    }
    
   private void populateUserAccountJTable()
    {
       
        DefaultTableModel dtm = (DefaultTableModel)userAccountJTable.getModel();
        dtm.setRowCount(0);
        
        for(Organization organization : business.getOrganizationDirectory().getOrganizationList())
        {
            for(UserAccount userAccount : organization.getUserAccountDirectory().getUserAccountList())
            {
            Object row[] = new Object[4];
            row[0] = userAccount;
            row[1] = userAccount.getPerson().getFirstName();
            row[2] = userAccount.getRole();
            row[3] = userAccount.getStatus();
            dtm.addRow(row);
        }
        }
    }
